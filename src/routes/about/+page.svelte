	<p>
		Each tarot card has multiple layers of meaning. This app will show you
		the following correspondences when you select a card:
	</p>
	<ul>
		<li><strong>Card Name</strong></li>
		<li><strong>Original Golden Dawn Title</strong></li>
		<li>
			<strong>Original Esoteric Title:</strong> Title given to the card by
			the Golden Dawn
		</li>
		<li>
			<strong>Astrological or Elemental Attribution:</strong> The symbol(s)
			which frame the energy of the card
		</li>
		<li>
			<strong>Hebrew Letter & Letter Association:</strong> Each Major Arcana
			card corresponds to a Hebrew letter and its symbolic meaning (e.g., Aleph
			= Ox, Beth = House)
		</li>
		<li>
			<strong>Part of Soul:</strong> The level of the psyche or soul the card
			touches (from the perspective of Kabbalah)
		</li>
		<li>
			<strong>Suit and Element (for Minor Arcana):</strong> The suit indicates
			the domain of life (e.g., Wands = creativity and will, Cups = emotions).
		</li>
		<li>
			<strong>Division of Creation:</strong> The Qabalistic world associated
			with the card (Atziluth, Briah, Yetzirah, Assiah).
		</li>
		<li>
			<strong>Tree of Life Path or Sefira:</strong> Shows where the card sits
			on the Kabbalistic Tree of Life—either a path connecting two sefirot
			(as in the case of the Major Arcana) or a sefira itself (as in the case
			of the minor arcana). For a more comprehensive understanding of the Tree
			of Life.
		</li>
		<li>
			<strong>Dates/Degrees of the Zodiac:</strong> In the case of the court
			cards, there are date-ranges the card corresponds to.
		</li>
		<li><strong>Court Card Birthday Examples</strong></li>
	</ul>

