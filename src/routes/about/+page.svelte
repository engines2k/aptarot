	<h2>Historical and Esoteric Roots</h2>

	<p>
		Tarot first appeared in 15th-century Italy as a card game <i
			>(trionfi)</i
		>, but by the 18th and 19th centuries was adapted for occult and
		esoteric use by groups such as the French occultists and the Hermetic
		Order of the Golden Dawn. These systems connected tarot to:
	</p>

	<p>
		<strong>Astrology:</strong> Cards correspond to planets, zodiac signs, and
		elements. This can also be found in the Rosicrucian symbol of the Rosy Cross.
	</p>

	<p>
		<strong>Kabbalah/Qabbalah:</strong> The Major Arcana map to the 22 paths
		of the Tree of Life and each have Hebrew letter correspondences.
	</p>

	<p>
		As far as history tells, tarot is not as old as the Egyptian or
		Babylonian mystery traditions (although some have made this claim). This
		said, it has, at the very least, absorbed their ancient symbolic
		systems, becoming a kind of living book of perennial wisdom.
	</p>

	<p>But only for those who can read the symbols.</p>
	<h2>Correspondences</h2>

	<div class="note">
		For a more comprehensive lesson on these correspondences, please see <a
			href="https://www.tarrdaniel.com/documents/Hermetic/tarot/tarot_info/tree_of_life.html"
